package org.example.g0401_0500.s0476_number_complement;

/**
 *  Решение с побитовым сдвигом
 *
 *  Объяснение:
 * Создание маски: Постепенно увеличиваем маску пока она
 * не покроет все значимые биты
 *
 * Пример для num=5:
 * mask = 1 (0001)
 * mask = (1<<1)|1 = 3 (0011)
 * mask = (3<<1)|1 = 7 (0111) - остановка
 *
 * Применение маски: ~5 & 7 = 11111010 & 00000111 = 00000010 = 2
 */
class Solution2 {
    public int findComplement(int num) {
        int mask = 1;

        // Находим маску: сдвигаем 1 пока она не станет больше num
        while (mask < num) {
            mask = (mask << 1) | 1;
        }

        // Инвертируем и применяем маску
        return ~num & mask;
    }
}
