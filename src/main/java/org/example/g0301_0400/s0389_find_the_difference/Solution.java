package org.example.g0301_0400.s0389_find_the_difference;

/**
 * Даны две строки s и t, состоящие из строчных английских букв.
 * Строка t генерируется путем случайного перемешивания строки
 * s и добавления одной дополнительной буквы в случайную позицию.
 * Необходимо найти добавленную букву.
 * Пример1
 * Вход: s = "abcd", t = "abcde"
 * Выход: "e"
 * Объяснение: Буква 'e' была добавлена.
 *
 * Пример 2
 * Вход: s = "", t = "y"
 * Выход: "y"
 * Объяснение: Поскольку s пустая, t состоит из одной добавленной буквы.
 *
 * Оптимальное решение - использование XOR
 *
 * todo разобраться подробнее
 */
class Solution {
    public char findTheDifference(String s, String t) {
        char result = 0;

        // XOR всех символов в строке s
        for (char c : s.toCharArray()) {
            result ^= c;
        }

        // XOR всех символов в строке t
        for (char c : t.toCharArray()) {
            result ^= c;
        }

        // Останется только добавленный символ
        return result;
    }
}
/*
Использование операции XOR - это эффективное решение, так как:
XOR одинаковых символов даст 0
В конце останется код добавленного символа
Временная сложность: O(n)
Пространственная сложность: O(1)
*/