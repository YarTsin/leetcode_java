/**
 * 520. Detect Capital
 *
 * Мы определяем правильное использование заглавных букв в слове одним из следующих способов:
 * Все буквы в слове заглавные (например, "USA")
 * Все буквы в слове строчные (например, "leetcode")
 * Только первая буква в слове заглавная (например, "Google")
 * Напишите функцию, которая определяет, правильно ли использованы
 * заглавные буквы в заданной строке word.
 *
 * Пример 1
 * Вход: word = "USA"
 * Выход: true
 * Объяснение: Все буквы заглавные - это допустимый вариант.
 *
 * Пример 2
 * Вход: word = "leetcode"
 * Выход: true
 * Объяснение: Все буквы строчные - это допустимый вариант.
 *
 * Пример 3
 * Вход: word = "Google"
 * Выход: true
 * Объяснение: Только первая буква заглавная - это допустимый вариант.
 *
 * Решение - с подсчетом заглавных букв - 2 место по скорости - 1 ms
 * Сложность:  * Время: O(n)  * Память: O(1)
 *
 */
class Solution {
    public boolean detectCapitalUse(String word) {
        int n = word.length();
        int upperCaseCount = 0;

        // Считаем количество заглавных букв
        for (int i = 0; i < n; i++) {
            if (Character.isUpperCase(word.charAt(i))) {
                upperCaseCount++;
            }
        }

        // Проверяем условия
        return upperCaseCount == 0 || // все строчные
                upperCaseCount == n || // все заглавные
                (upperCaseCount == 1 && Character.isUpperCase(word.charAt(0))); // только первая заглавная
    }
}
/*
Подход:
Посчитаем количество заглавных букв в слове и проверим условия:
Если количество заглавных букв равно 0 - все строчные ✓
Если количество заглавных букв равно длине слова - все заглавные ✓
Если количество заглавных букв равно 1 и первая буква заглавная - только первая заглавная ✓
Во всех остальных случаях - неверно
 */
