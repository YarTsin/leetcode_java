package org.example.g0501_0600.s0520_detect_capital;

/**
 * Решение с использованием регулярных выражений
 *
 * Подход:
 * Используем регулярные выражения для проверки трех допустимых паттернов.
 * Паттерны:
 * [A-Z]* - все заглавные
 * [a-z]* - все строчные
 * [A-Z][a-z]* - первая заглавная, остальные строчные
 *
 * Сложность:
 * Время: O(n) (регулярные выражения компилируются один раз)
 * Память: O(1)
 *
 */
class Solution4 {
    public boolean detectCapitalUse(String word) {
        return word.matches("[A-Z]*") ||
                word.matches("[a-z]*") ||
                word.matches("[A-Z][a-z]*");
    }
}