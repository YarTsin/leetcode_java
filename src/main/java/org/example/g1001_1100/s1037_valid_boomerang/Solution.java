package org.example.g1001_1100.s1037_valid_boomerang;

// #Easy #Array #Math #Geometry #Vector


/**
 * 1037. Valid Boomerang
 * Условие задачи:
 * Даны три точки на плоскости в виде массива points,
 * где points[i] = [xi, yi]. Необходимо определить, образуют ли эти
 * три точки бумеранг.
 *
 * Бумеранг определяется как набор из трех точек, которые не являются
 * коллинеарными (не лежат на одной прямой). Другими словами, три точки
 * образуют бумеранг, если они образуют треугольник с ненулевой площадью.
 * (примеры внизу)
 *
 * Решение: математическое - через площадь треугольника
 * самое быстрое - 0 ms
 *
 * Сложность:
 * Временная сложность: O(1) - фиксированное количество вычислений
 * Пространственная сложность: O(1) - константное использование памяти
 *
 */
class Solution {
    public boolean isBoomerang(int[][] points) {
        // Извлекаем координаты трех точек
        int x1 = points[0][0], y1 = points[0][1];
        int x2 = points[1][0], y2 = points[1][1];
        int x3 = points[2][0], y3 = points[2][1];

        // Проверяем, что все точки различны
        // Если любые две точки совпадают, это не бумеранг
        if ((x1 == x2 && y1 == y2) ||
                (x2 == x3 && y2 == y3) ||
                (x1 == x3 && y1 == y3)) {
            return false;
        }

        // Проверяем коллинеарность через площадь треугольника
        // Если площадь равна 0, точки лежат на одной прямой
        int area = (x1 - x2) * (y2 - y3) - (x2 - x3) * (y1 - y2);

        // Возвращаем true если площадь не равна 0
        return area != 0;
    }
}

/*
Пример 1
Ввод: points = [[1,1],[2,3],[3,2]]
Вывод: true
Объяснение:
Три точки [1,1], [2,3], [3,2] не лежат на одной прямой.
Они образуют треугольник с площадью 0.5 (ненулевая).

Пример 2
Ввод: points = [[1,1],[2,2],[3,3]]
Вывод: false
Объяснение:
Три точки [1,1], [2,2], [3,3] лежат на одной прямой y = x.
Они образуют вырожденный треугольник с нулевой площадью.

Пример 3
Ввод: points = [[0,0],[1,1],[1,1]]
Вывод: false
Объяснение:
Две точки совпадают [1,1] и [1,1], поэтому точки
не могут образовать бумеранг.
 */