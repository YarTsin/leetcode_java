/**
 * 1742. Maximum Number of Balls in a Box
 *
 * Есть коробки с номерами от 1 до n. Мы бросаем шарики в коробки
 * по следующему правилу: для каждого целого числа i в диапазоне
 * [lowLimit, highLimit] мы бросаем шарик в коробку с номером,
 * равным сумме цифр числа i.
 *
 * Например:
 * Число 123 попадает в коробку 1+2+3 = 6
 * Число 10 попадает в коробку 1+0 = 1
 * Число 99 попадает в коробку 9+9 = 18
 * Нужно найти максимальное количество шариков в одной коробке.
 * (примеры внизу)
 *
 * Решение -  с использованием массива для подсчета
 * Решение не быстрое - 14 ms
 *
 * Подход:
 * Используем массив для подсчета количества шариков в каждой коробке.
 * Максимальная возможная сумма цифр для чисел до 10^5 = 99999
 * составляет 9+9+9+9+9 = 45.
 *
 * Сложность:
 * Время: O(n) где n = highLimit - lowLimit
 * Память: O(1) - массив фиксированного размера (46 элементов)
 */
class Solution {
    public int countBalls(int lowLimit, int highLimit) {
        // Максимальная возможная сумма цифр для чисел до 100000 = 9+9+9+9+9 = 45
        int[] boxCount = new int[46]; // индексы 0-45

        for (int i = lowLimit; i <= highLimit; i++) {
            int boxNumber = calculateDigitSum(i);
            boxCount[boxNumber]++;
        }

        // Находим максимальное значение в массиве
        int maxBalls = 0;
        for (int count : boxCount) {
            if (count > maxBalls) {
                maxBalls = count;
            }
        }

        return maxBalls;
    }

    // Вспомогательная функция для вычисления суммы цифр числа
    private int calculateDigitSum(int number) {
        int sum = 0;
        while (number > 0) {
            sum += number % 10;
            number /= 10;
        }
        return sum;
    }
}
