package org.example.g1701_1800.s1784_check_if_binary_string_has_at_most_one_segment_of_ones;

// #Easy #String #Bits

/**
 * 1784. Check if Binary String Has at Most One Segment of Ones
 *
 * Дана бинарная строка s (состоящая только из символов '0' и '1').
 * Необходимо проверить, содержит ли строка не более одного сегмента единиц.
 * Сегмент единиц - это смежная (непрерывная) последовательность символов '1',
 * которая не может быть расширена ни влево, ни вправо
 * (то есть ограничена символами '0' или концами строки).
 *
 * Важные уточнения:
 * Строка гарантированно начинается с символа '1'.
 * Нужно проверить, что после первого сегмента единиц нет других
 * сегментов единиц в строке.
 * Если строка состоит только из единиц - это один сегмент.
 * Если после первого сегмента единиц встречаются нули,
 * а потом снова единицы - это уже два сегмента.
 * (примеры внизу)
 *
 * Решение - поиск первого вхождения "01" в строке
 * Самое быстрое - 0 ms
 */
class Solution {
    public boolean checkOnesSegment(String s) {
        // Поскольку строка гарантированно начинается с '1',
        // наличие подстроки "01" будет означать, что:
        // - '0' отмечает конец первого сегмента единиц
        // - '1' после '0' marks начало второго сегмента единиц
        // Следовательно, если "01" найдена, сегментов как минимум два

        // Метод indexOf возвращает -1, если подстрока не найдена
        // Если "01" не найдена, возвращаем true (не более одного сегмента)
        // Если "01" найдена, возвращаем false (два или более сегментов)
        return s.indexOf("01") == -1;

        // Альтернативная запись:
        // return !s.contains("01");
    }
}
/*
Пример 1
Вход: s = "1001"
Результат: false
Объяснение:
Первый сегмент: "1" (индексы 0-0)
Затем нули: "00"
Второй сегмент: "1" (индекс 3)
Два сегмента единиц → результат false

Пример 2
Вход: s = "110"
Результат: true
Объяснение:
Единственный сегмент: "11" (индексы 0-1)
Затем ноль: "0"
Только один сегмент единиц → результат true

Пример 3
Вход: s = "1"
Результат: true
Объяснение: Только один символ '1' - один сегмент.
 */