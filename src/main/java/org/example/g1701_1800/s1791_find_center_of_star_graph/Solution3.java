package org.example.g1701_1800.s1791_find_center_of_star_graph;

/**
 * Подсчет степеней вершин
 * Это решение подсчитывает, сколько раз каждая вершина встречается в ребрах.
 *
 * Более общее решение, которое работает для любых графов.
 * Временная сложность O(E), где E - количество ребер.
 */
class Solution3 {
    public int findCenter(int[][] edges) {
        // Поскольку вершины нумеруются от 1 до n, и n = edges.length + 1
        int n = edges.length + 1;

        // Создаем массив для подсчета степени каждой вершины
        int[] degree = new int[n + 1]; // индексы 1..n

        // Подсчитываем степени вершин
        for (int[] edge : edges) {
            int u = edge[0];
            int v = edge[1];
            degree[u]++;
            degree[v]++;
        }

        // Ищем вершину со степенью n-1 (центр звезды)
        for (int i = 1; i <= n; i++) {
            if (degree[i] == n - 1) {
                return i;
            }
        }

        return -1; // Не должно произойти для валидной звезды
    }
}
