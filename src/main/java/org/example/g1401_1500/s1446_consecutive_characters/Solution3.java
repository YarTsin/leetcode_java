package org.example.g1401_1500.s1446_consecutive_characters;

/**
 * Решение с использованием группы символов
 *
 * Объяснение подхода:
 * Группировка символов: Находим границы последовательностей одинаковых символов
 * Внешний цикл: Проходим по всем символам строки
 * Внутренний цикл: Расширяем последовательность, пока символы одинаковые
 * Вычисление длины: Длина = j - i + 1
 * Переход к следующей группе: Устанавливаем i = j + 1
 *
 * Временная сложность: O(n) - каждый символ посещается один раз
 * Пространственная сложность: O(1)
 */
class Solution3 {
    public int maxPower(String s) {
        int maxLength = 1;
        int n = s.length();

        int i = 0;
        while (i < n) {
            int j = i;
            // Находим конец текущей последовательности одинаковых символов
            while (j < n - 1 && s.charAt(j) == s.charAt(j + 1)) {
                j++;
            }
            // Вычисляем длину последовательности
            int currentLength = j - i + 1;
            maxLength = Math.max(maxLength, currentLength);
            i = j + 1; // Переходим к следующему символу
        }

        return maxLength;
    }
}
