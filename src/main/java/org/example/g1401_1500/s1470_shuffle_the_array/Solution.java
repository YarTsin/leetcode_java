/**
 * 1470. Shuffle the Array
 *
 * Дан массив nums состоящий из 2n элементов в формате
 * [x₁, x₂, ..., xₙ, y₁, y₂, ..., yₙ]. Необходимо перетасовать
 * массив в формате [x₁, y₁, x₂, y₂, ..., xₙ, yₙ].
 * (n - это половина по количеству элементов)
 *
 * Другими словами, нужно взять первые n элементов (x-элементы)
 * и вторые n элементов (y-элементы), а затем чередовать их,
 * начиная с первого x-элемента, затем первого y-элемента,
 * второго x-элемента, второго y-элемента и так далее.
 * (примеры внизу)
 *
 * Constraints:
 * `1 <= n <= 500`
 * `nums.length == 2n`
 * `1 <= nums[i] <= 10^3`
 *
 * Решение - прямое заполнение результирующего массива
 * Самое быстрое - 0 ms
 *
 * Подход:
 * Создаем новый массив такой же длины и заполняем его,
 * чередуя элементы из первой половины (x-элементы) и второй
 * половины (y-элементы) исходного массива.
 *
 * Сложность: O(n) по времени, O(n) по памяти (для результирующего массива)
 *
 */
class Solution {
    public int[] shuffle(int[] nums, int n) {
        // Создаем результирующий массив такой же длины
        int[] result = new int[2 * n];

        // Индекс для результирующего массива
        int resultIndex = 0;

        // Проходим по первой половине массива (x-элементы)
        for (int i = 0; i < n; i++) {
            // Добавляем x-элемент
            result[resultIndex++] = nums[i];
            // Добавляем соответствующий y-элемент
            result[resultIndex++] = nums[i + n];
        }

        return result;
    }
}
