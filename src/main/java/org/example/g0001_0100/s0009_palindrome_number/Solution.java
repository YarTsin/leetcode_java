package org.example.g0001_0100.s0009_palindrome_number;

/**
 * #Easy #Math
 * Является ли целое число палиндромом.
 * Палиндром - это число (или строка), которое читается одинаково в обоих направлениях.
 *
 * Примеры:
 * 121 → true
 * -121 → false (из-за минуса)
 * 10 → false
 */
class Solution {
    /**
     * Проверяет, является ли число палиндромом
     * @param x число для проверки
     * @return true, если число является палиндромом, иначе false
     */
    public boolean isPalindrome(int x) {
        // Отрицательные числа не могут быть палиндромами
        // Также числа, оканчивающиеся на 0 (кроме 0) не могут быть палиндромами
        if (x < 0 || (x % 10 == 0 && x != 0)) {
            return false;
        }

        int reversedHalf = 0;

        // Переворачиваем половину числа
        while (x > reversedHalf) {
            int lastDigit = x % 10; // Получаем последнюю цифру
            reversedHalf = reversedHalf * 10 + lastDigit; // Добавляем цифру к перевернутой половине
            x /= 10; // Удаляем последнюю цифру из исходного числа
        }

        // Для чисел с четным количеством цифр x == reversedHalf
        // Для нечетного количества цифр x == reversedHalf / 10 (средняя цифра не важна)
        return x == reversedHalf || x == reversedHalf / 10;
    }
}