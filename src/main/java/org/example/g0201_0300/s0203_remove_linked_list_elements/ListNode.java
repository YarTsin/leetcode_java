package org.example.g0201_0300.s0203_remove_linked_list_elements;


public class ListNode {
     int val;
     ListNode next;

     ListNode() {
     }

     ListNode(int val) {
          this.val = val;
     }

     ListNode(int val, ListNode next) {
          this.val = val;
          this.next = next;
     }
}
