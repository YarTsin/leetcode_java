package org.example.g0201_0300.s0206_reverse_linked_list;


public class ListNode {
     int val;
     ListNode next;

     ListNode() {
     }

     ListNode(int val) {
          this.val = val;
     }

     ListNode(int val, ListNode next) {
          this.val = val;
          this.next = next;
     }
}
