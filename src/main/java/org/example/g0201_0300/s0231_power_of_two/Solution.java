package org.example.g0201_0300.s0231_power_of_two;

/**
 * #Easy #Math  Степень числа
 * Задача: является ли заданное целое число степенью двойки.
 * Примеры:
 * 1 → true (2^0)
 * 16 → true (2^4)
 * 3 → false
 * 0 → false (по определению, 0 не является степенью двойки)
 *
 * Оптимальное решение: битовые операции
 */
class Solution {
    public boolean isPowerOfTwo(int n) {
        // Числа <= 0 не могут быть степенями двойки
        if (n <= 0) {
            return false;
        }

        // Степень двойки в битовом представлении имеет только одну единицу
        // n & (n - 1) обнуляет младший значащий бит
        // Для степени двойки результат будет 0
        return (n & (n - 1)) == 0;
    }
}