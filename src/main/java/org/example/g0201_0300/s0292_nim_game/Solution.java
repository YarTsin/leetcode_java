package org.example.g0201_0300.s0292_nim_game;


/**
 * Игра Ним - это математическая стратегическая игра, где два игрока
 * по очереди берут предметы из кучи. В данной версии:
 * Имеется n камней в куче
 * Каждый игрок может взять 1, 2 или 3 камня за ход
 * Игрок, который берет последний камень, побеждает
 * Вы начинаете игру первым
 * Необходимо определить, можно ли гарантированно выиграть при заданном n,
 * если оба игрока играют оптимально.
 *
 * Пример:
 * Input: n = 4
 * Output: false
 * Explanation:
 * 1. Вы берете 1 камень, остается 3
 * 2. Оппонент берет 3 камня и побеждает
 * (Аналогично для других ваших ходов)
 *
 * Оптимальное решение - математический подход
 *
 * todo завести категорию Игры?
 */
public class Solution {
    public boolean canWinNim(int n) {
        // Если количество камней не кратно 4, можно гарантировать победу
        return n % 4 != 0;
    }
}