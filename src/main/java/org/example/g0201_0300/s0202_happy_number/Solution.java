package org.example.g0201_0300.s0202_happy_number;

import java.util.HashSet;
import java.util.Set;

/**
 * LeetCode Easy
 * Нам нужно определить, является ли число "счастливым".
 * Счастливое число - это число, которое в процессе замены суммы
 * квадратов его цифр в конечном итоге достигает 1.
 * Если процесс зацикливается и не достигает 1,
 * то число не является счастливым.
 *
 * todo не очень понятно
 */

class Solution {
    public boolean isHappy(int n) {
        // Создаем множество для отслеживания уже встречавшихся чисел
        Set<Integer> seenNumbers = new HashSet<>();

        // Продолжаем процесс, пока не достигнем 1 или не обнаружим цикл
        while (n != 1 && !seenNumbers.contains(n)) {
            // Добавляем текущее число в множество
            seenNumbers.add(n);
            // Вычисляем следующее число как сумму квадратов цифр
            n = getNextNumber(n);
        }

        // Если остановились на 1 - число счастливое
        return n == 1;
    }

    // Вспомогательный метод для вычисления суммы квадратов цифр числа
    private int getNextNumber(int n) {
        int sum = 0;
        while (n > 0) {
            int digit = n % 10; // Получаем последнюю цифру
            sum += digit * digit; // Добавляем квадрат цифры к сумме
            n = n / 10; // Убираем последнюю цифру из числа
        }
        return sum;
    }
}