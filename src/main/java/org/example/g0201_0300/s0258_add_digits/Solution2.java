package org.example.g0201_0300.s0258_add_digits;

/**
 * Итеративный подход - для новичков
 * Временная сложность: O(log n) для каждой итерации (в худшем случае несколько итераций)
 */
class Solution2 {
    public int addDigits(int num) {
        // Пока число состоит из двух и более цифр
        while (num >= 10) {
            int sum = 0;
            // Разбиваем число на цифры и суммируем их
            while (num > 0) {
                sum += num % 10; // Получаем последнюю цифру
                num /= 10;      // Убираем последнюю цифру из числа
            }
            num = sum; // Присваиваем сумму для следующей итерации
        }
        return num;
    }
}