package org.example.g1801_1900.s1812_determine_color_of_a_chessboard_square;

// #Easy #String #Math #Chess #Bits

/**
 * 1812. Determine Color of a Chessboard Square
 *
 * Дана строка coordinates, представляющая координаты шахматной клетки
 * в стандартной шахматной нотации. Строка состоит из 2 символов:
 * Первый символ - строчная английская буква от 'a' до 'h' (столбец)
 * Второй символ - цифра от '1' до '8' (строка)
 * Нужно определить, является ли данная клетка черной или белой.
 * Вернуть true, если клетка белая, и false, если черная.
 *
 * Шахматная доска:
 * Стандартная шахматная доска 8×8 клеток
 * Клетка a1 (левый нижний угол) - черная
 * Цвета чередуются в шахматном порядке
 * (примеры внизу)
 *
 * Решение - математическое - самое быстрое 0 ms
 *
 * Подход: Используем математическое свойство шахматной доски.
 * Если сумма числовых значений столбца и строки четная
 * - клетка черная, если нечетная - белая.
 *
 */
class Solution {
    public boolean squareIsWhite(String coordinates) {
        // Преобразуем букву столбца в число: 'a'->1, 'b'->2, ..., 'h'->8
        int column = coordinates.charAt(0) - 'a' + 1;
        // Преобразуем цифру строки в число
        int row = coordinates.charAt(1) - '0';

        // Если сумма четная - черная клетка (false), нечетная - белая (true)
        return (column + row) % 2 != 0;
    }
}
/*
Пример 1
Input: coordinates = "a1"
Output: false
Объяснение: a1 - черная клетка

Пример 2
Input: coordinates = "h3"
Output: true
Объяснение: h3 - белая клетка
 */