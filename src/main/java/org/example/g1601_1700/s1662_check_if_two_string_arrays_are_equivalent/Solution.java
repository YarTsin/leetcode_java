package org.example.g1601_1700.s1662_check_if_two_string_arrays_are_equivalent;

// #Easy #Array #String #Points

/**
 * 1662. Check If Two String Arrays are Equivalent
 *
 * Даны два массива строк word1 и word2. Необходимо определить,
 * представляют ли они одну и ту же строку при конкатенации
 * всех элементов массива.
 *
 * Ключевые моменты:
 * Два массива считаются эквивалентными, если конкатенация всех
 * элементов word1 равна конкатенации всех элементов word2
 * Нельзя просто соединить все строки и сравнить (это может быть
 * неэффективно для больших массивов)
 * Нужно решение, которое не создает новые строки конкатенации
 * (примеры внизу)
 *
 * Решение - с указателями - 4-е по скорости - 3 ms
 *
 * Временная сложность: O(n + m) где n, m - общее количество символов
 * Пространственная сложность: O(1) - не используем дополнительной памяти
 */
class Solution {
    public boolean arrayStringsAreEqual(String[] word1, String[] word2) {
        // Указатели для word1: i1 - индекс слова, j1 - индекс символа в слове
        int i1 = 0, j1 = 0;
        // Указатели для word2: i2 - индекс слова, j2 - индекс символа в слове
        int i2 = 0, j2 = 0;

        while (i1 < word1.length && i2 < word2.length) {
            // Получаем текущие символы из обоих массивов
            char char1 = word1[i1].charAt(j1);
            char char2 = word2[i2].charAt(j2);

            // Если символы не равны - массивы не эквивалентны
            if (char1 != char2) {
                return false;
            }

            // Перемещаем указатели вперед
            j1++;
            j2++;

            // Если достигли конца слова в word1, переходим к следующему слову
            if (j1 == word1[i1].length()) {
                i1++;
                j1 = 0;
            }

            // Если достигли конца слова в word2, переходим к следующему слову
            if (j2 == word2[i2].length()) {
                i2++;
                j2 = 0;
            }
        }

        // Проверяем, что оба массива полностью пройдены
        return i1 == word1.length && i2 == word2.length;
    }
}
/*
Пример 1
Вход: word1 = ["ab", "c"], word2 = ["a", "bc"]
Выход: true
Объяснение:
word1 представляет строку "ab" + "c" = "abc"
word2 представляет строку "a" + "bc" = "abc"

Пример 2
Вход: word1 = ["a", "cb"], word2 = ["ab", "c"]
Выход: false

Пример 3
Вход: word1  = ["abc", "d", "defg"], word2 = ["abcddefg"]
Выход: true
 */