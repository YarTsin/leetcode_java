/**
 * 1332. Remove Palindromic Subsequences
 *
 * Дана строка s, состоящая только из букв 'a' и 'b'.
 * Можно удалять палиндромные подпоследовательности из s за один шаг.
 *
 * Важные определения:
 * Подпоследовательность - последовательность, которую можно
 * получить из исходной строки удалением некоторых символов без
 * изменения порядка оставшихся символов
 *
 * Палиндромная подпоследовательность - подпоследовательность,
 * которая читается одинаково вперед и назад
 * (примеры внизу)
 *
 * Вернуть минимальное количество шагов, чтобы сделать строку пустой.
 *
 * Решение 1 - самое быстрое - 0 ms
 *
 * Ключевое наблюдение: Так как строка состоит только
 * из 'a' и 'b', ответ может быть только 0, 1 или 2.
 *
 * Логика:
 * Если строка пуста - вернуть 0
 * Если вся строка палиндром - вернуть 1 (можно удалить всю строку за один шаг)
 * Иначе вернуть 2 (можно удалить все 'a' за один шаг и все 'b' за второй шаг)
 *
 */
class Solution {
    public int removePalindromeSub(String s) {
        // Пустая строка - 0 шагов
        if (s.isEmpty()) {
            return 0;
        }

        // Проверяем, является ли вся строка палиндромом
        if (isPalindrome(s)) {
            return 1; // Можно удалить всю строку за один шаг
        }

        return 2; // Удаляем все 'a', затем все 'b' (или наоборот)
    }

    // Вспомогательная функция для проверки палиндрома
    private boolean isPalindrome(String s) {
        int left = 0;
        int right = s.length() - 1;

        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }
        return true;
    }
}
