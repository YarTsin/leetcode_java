/**
 * 1374. Generate a String With Characters That Have Odd Counts
 *
 * Дано целое число n. Необходимо вернуть строку, состоящую из
 * n символов, такая, что каждый символ в этой строке встречается
 * нечётное количество раз.
 *
 * В возвращаемой строке могут быть только строчные буквы английского
 * алфавита ('a' - 'z'). Если возможных решений несколько,
 * можно вернуть любое из них.
 *
 * Проще говоря: Нужно создать строку длиной n, и для каждой буквы
 * в этой строке (например, для 'a', 'b' и т.д.) общее количество
 * её вхождений во всю строку должно быть нечётным (1, 3, 5, ...).
 * (примеры внизу)
 *
 * Решение на анализе четности числа - 2-е решение - 1 ms
 *
 * Временная сложность: O(n). Мы выполняем цикл либо n раз, либо n-1 раз.
 * Пространственная сложность: O(n). Мы создаём строку длиной n символов.
 */
class Solution {
    public String generateTheString(int n) {
        // Создаем объект StringBuilder для эффективной конкатенации строк
        StringBuilder result = new StringBuilder();

        // Проверяем, является ли n четным числом
        if (n % 2 == 0) {
            // Если n четное:
            // Добавляем 'a' (n - 1) раз.
            // (n - 1) будет нечетным, так как мы вычитаем 1 из четного числа.
            for (int i = 0; i < n - 1; i++) {
                result.append('a');
            }
            // Добавляем один символ 'b', чтобы общая длина стала n.
            // Теперь у нас есть один символ с нечетным счетчиком (1) и
            // другой символ также с нечетным счетчиком (n-1).
            result.append('b');
        } else {
            // Если n нечетное:
            // Мы можем просто использовать один символ 'a' n раз.
            // Его счетчик (n) будет нечетным.
            for (int i = 0; i < n; i++) {
                result.append('a');
            }
        }

        // Преобразуем StringBuilder в String и возвращаем результат
        return result.toString();
    }
}
