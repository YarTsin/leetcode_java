package org.example.g1201_1300.s1295_find_numbers_with_even_number_of_digits;

/**
 * Математическая оптимизация - Использование диапазонов чисел
 * Основная идея: Вместо подсчета цифр, проверять в каких
 * диапазонах находится число.
 *
 * Подход:
 * Числа с четным количеством цифр находятся в диапазонах:
 * [10, 99] → 2 цифры
 * [1000, 9999] → 4 цифры
 * [100000, ∞] → 6+ цифр
 *
 * Сложность:
 * Время: O(n)
 * Память: O(1)
 */
public class Solution5 {
    public int findNumbers(int[] nums) {
        int count = 0;

        for (int num : nums) {
            // Проверяем диапазоны для четного количества цифр
            if ((num > 9 && num < 100) ||     // 2 цифры
                    (num > 999 && num < 10000) || // 4 цифры
                    (num == 100000)) {            // 6 цифр (максимальное по условию)
                count++;
            }
        }

        return count;
    }
}
