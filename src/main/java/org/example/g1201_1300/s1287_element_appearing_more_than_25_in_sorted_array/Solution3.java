package org.example.g1201_1300.s1287_element_appearing_more_than_25_in_sorted_array;

/**
 * Подсчет частот с использованием преимущества сортировки
 * Основная идея: Воспользоваться тем, что массив отсортирован
 * - одинаковые элементы идут подряд
 *
 * Подход:
 * Пройти по массиву, подсчитывая длину текущей последовательности
 * Если длина последовательности > n/4, вернуть элемент
 * При смене элемента сбросить счетчик
 *
 * Сложность:
 * Время: O(n)
 * Память: O(1)
 *
 */
public class Solution3 {
    public int findSpecialInteger(int[] arr) {
        int n = arr.length;
        int threshold = n / 4;
        int count = 1;

        // Проходим по массиву начиная со второго элемента
        for (int i = 1; i < n; i++) {
            if (arr[i] == arr[i - 1]) {
                count++;
            } else {
                // Проверяем предыдущую последовательность
                if (count > threshold) {
                    return arr[i - 1];
                }
                count = 1; // Сбрасываем счетчик для нового элемента
            }
        }

        // Проверяем последнюю последовательность
        if (count > threshold) {
            return arr[n - 1];
        }

        return -1;
    }
}