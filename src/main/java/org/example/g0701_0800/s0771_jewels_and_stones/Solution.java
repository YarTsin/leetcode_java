package org.example.g0701_0800.s0771_jewels_and_stones;

// #Easy #String #Hash_Table #Boolean


/**
 * 771. Jewels and Stones
 * Даны две строки: jewels (драгоценности) и stones (камни).
 * Строка jewels представляет собой набор видов драгоценных камней.
 * Каждый символ в jewels является уникальным и представляет
 * собой один вид драгоценности. Строка stones представляет собой камни,
 * которые у вас есть. Каждый символ в stones — это камень,
 * который может быть либо драгоценностью (если его тип присутствует
 * в jewels), либо обычным камнем.
 *
 * Цель: Посчитать, сколько камней в строке stones являются
 * драгоценностями. Другими словами, нужно посчитать количество
 * символов в stones, которые присутствуют в строке jewels.
 *
 * Важные детали:
 * Регистр имеет значение. Символ 'a' и 'A' считаются разными типами камней.
 * Все символы в jewels уникальны.
 * Длины строк могут быть до 50 символов.
 * (примеры внизу)
 *
 * Решение - Использование HashSet для драгоценностей
 * 2-е по скорости решение - 1 ms
 *
 */
import java.util.HashSet;
import java.util.Set;

class Solution {
    public int numJewelsInStones(String jewels, String stones) {
        // Создаем множество для хранения видов драгоценных камней
        Set<Character> jewelSet = new HashSet<>();

        // Заполняем множество символами из строки jewels
        for (char jewel : jewels.toCharArray()) {
            jewelSet.add(jewel);
        }

        // Инициализируем счетчик драгоценных камней
        int count = 0;

        // Проходим по всем камням в строке stones
        for (char stone : stones.toCharArray()) {
            // Если текущий камень является драгоценностью (содержится в множестве)
            if (jewelSet.contains(stone)) {
                count++; // Увеличиваем счетчик
            }
        }

        return count;
    }
}
/*
Пример 1
Ввод: jewels = "aA", stones = "aAAbbbb"
Вывод: 3
Объяснение:
Драгоценности: 'a' и 'A'.
Камни: 'a', 'A', 'A', 'b', 'b', 'b', 'b'.
Драгоценные камни: первый 'a', первый 'A', второй 'A'.
Итого: 3 драгоценных камня.

Пример 2
Ввод: jewels = "z", stones = "ZZ"
Вывод: 0
Объяснение:
Драгоценности: 'z'.
Камни: 'Z', 'Z'.
Символ 'Z' (верхний регистр) не равен символу 'z' (нижний регистр).
Драгоценных камней нет.
*/