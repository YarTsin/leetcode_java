package org.example.g0701_0800.s0766_toeplitz_matrix;

// #Easy #Array #Matrix #Toeplitz_Matrix

/**
 * 766. Toeplitz Matrix
 * Условие: Дана двумерная матрица matrix размером m x n.
 * Нужно определить, является ли эта матрица матрицей
 * Тёплица (Toeplitz matrix).
 *
 * Что такое матрица Тёплица?
 * Матрица является матрицей Тёплица, если каждая ее диагональ,
 * идущая из левого верхнего в правый нижний угол,
 * состоит из одинаковых элементов. Другими словами, для любого
 * элемента матрицы matrix[i][j] должен выполняться условие:
 * matrix[i][j] == matrix[i + 1][j + 1] (при условии,
 * что индексы i + 1 и j + 1 находятся в пределах матрицы).
 *
 * Проще говоря: Все элементы на любой диагонали
 * (идущей вниз-вправо) должны быть равны.
 * (примеры внизу)
 *
 * Решение: Последовательное сравнение элементов - самое быстрое 0 ms
 *
 */
class Solution {
    public boolean isToeplitzMatrix(int[][] matrix) {
        // Получаем количество строк (m) и столбцов (n) в матрице
        int numRows = matrix.length;
        int numCols = matrix[0].length;

        // Проходим по всем элементам матрицы, кроме последней строки и последнего столбца.
        // Для каждого элемента [i][j] проверяем, равен ли он элементу [i+1][j+1].
        for (int row = 0; row < numRows - 1; row++) {
            for (int col = 0; col < numCols - 1; col++) {
                // Если текущий элемент не равен элементу по диагонали справа внизу...
                if (matrix[row][col] != matrix[row + 1][col + 1]) {
                    // ...значит, матрица не является матрицей Тёплица.
                    return false;
                }
            }
        }
        // Если мы проверили все возможные пары и не нашли несоответствий,
        // возвращаем true.
        return true;
    }
}
