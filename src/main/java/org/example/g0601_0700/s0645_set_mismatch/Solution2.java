package org.example.g0601_0700.s0645_set_mismatch;

/**
 * Альтернативное решение (без дополнительной памяти)
 */
class Solution2 {
    public int[] findErrorNums(int[] nums) {
        int[] result = new int[2];

        // Используем знак для отметки встреченных чисел
        for (int num : nums) {
            int index = Math.abs(num) - 1;
            if (nums[index] < 0) {
                result[0] = Math.abs(num);  // Дубликат
            } else {
                nums[index] = -nums[index];
            }
        }

        // Находим пропущенное число (единственное с положительным знаком)
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] > 0) {
                result[1] = i + 1;
                break;
            }
        }

        return result;
    }
}
/*
Объяснение:
Маркировка чисел:
Используем знак элементов массива для отметки встреченных чисел
Если число уже отрицательное - значит, мы его уже встречали (дубликат)

Поиск пропуска:
После обработки всех чисел, пропущенное число будет соответствовать
индексу единственного положительного элемента

Временная сложность: O(n)
Пространственная сложность: O(1) (модифицируем входной массив)
 */
