package org.example.g0801_0900.s0836_rectangle_overlap;

/**
 * Решение с явной проверкой координат
 */
class Solution3 {
    public boolean isRectangleOverlap(int[] rec1, int[] rec2) {
        // Проверяем, что левая граница второго прямоугольника меньше правой границы первого
        // И правая граница второго прямоугольника больше левой границы первого
        boolean xOverlap = rec2[0] < rec1[2] && rec2[2] > rec1[0];

        // Проверяем, что нижняя граница второго прямоугольника меньше верхней границы первого
        // И верхняя граница второго прямоугольника больше нижней границы первого
        boolean yOverlap = rec2[1] < rec1[3] && rec2[3] > rec1[1];

        return xOverlap && yOverlap;
    }
}
