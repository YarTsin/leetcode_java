package org.example.g0801_0900.s0836_rectangle_overlap;

// #Easy #Math #Geometry

/**
 * 836. Rectangle Overlap
 * Даны два оси-выровненных прямоугольника. Каждый прямоугольник представлен
 * списком координат [x1, y1, x2, y2], где:
 * (x1, y1) - координаты левого нижнего угла
 * (x2, y2) - координаты правого верхнего угла
 * Необходимо определить, пересекаются ли прямоугольники.
 * Если они имеют положительную площадь пересечения, возвращаем true, иначе false.
 *
 * Важные уточнения:
 * Прямоугольники считаются пересекающимися, только если их пересечение
 * имеет положительную площадь
 * Соприкосновение только по сторонам или углам НЕ считается пересечением
 * Прямоугольники оси-выровнены (их стороны параллельны осям X и Y)
 * (Примеры внизу)
 *
 * Решение - проверка условий пересечения - самое быстрое - 0ms
 */
class Solution {
    public boolean isRectangleOverlap(int[] rec1, int[] rec2) {
        // Проверяем условия, когда прямоугольники НЕ пересекаются
        // Прямоугольник rec2 находится слева от rec1
        boolean isLeft = rec2[2] <= rec1[0];

        // Прямоугольник rec2 находится справа от rec1
        boolean isRight = rec2[0] >= rec1[2];

        // Прямоугольник rec2 находится ниже rec1
        boolean isBelow = rec2[3] <= rec1[1];

        // Прямоугольник rec2 находится выше rec1
        boolean isAbove = rec2[1] >= rec1[3];

        // Если любое из этих условий true - прямоугольники не пересекаются
        // Возвращаем отрицание (если НЕТ условий непересечения, значит есть пересечение)
        return !(isLeft || isRight || isBelow || isAbove);
    }
}
/*
Пример 1
Вход: rec1 = [0,0,2,2], rec2 = [1,1,3,3]
Выход: true
Объяснение: Прямоугольники пересекаются по площади [1,1,2,2]

Пример 2
Вход: rec1 = [0,0,1,1], rec2 = [1,0,2,1]
Выход: false
Объяснение: Прямоугольники соприкасаются по стороне, но не имеют общей площади

Пример 3
Вход: rec1 = [0,0,1,1], rec2 = [2,2,3,3]
Выход: false
Объяснение: Прямоугольники не пересекаются
 */