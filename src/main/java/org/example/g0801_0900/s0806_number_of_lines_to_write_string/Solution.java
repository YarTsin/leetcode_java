// #Easy #Array #String

/**
 * 806. Number of Lines To Write String
 * Вам дается строка s из строчных английских букв и массив widths
 * из 26 целых чисел, где widths[i] представляет ширину
 * символа i-ой буквы в алфавите (т.е., widths[0] - ширина 'a',
 * widths[1] - ширина 'b', и так далее).
 *
 * Вы пытаетесь записать строку s across multiple lines, где
 * на каждой строке может быть не более 100 пикселей.
 * Начиная с первой строки, вы записываете как можно больше
 * символов на каждой строке, пока не исчерпаете всю строку s.
 * Возвращаете массив result длины 2, где:
 * result[0] - количество строк
 * result[1] - ширина последней строки в пикселях
 * (примеры внизу)
 *
 * Ограничения:
 * widths.length == 26
 * 2 <= widths[i] <= 10
 * 1 <= s.length <= 1000
 * s содержит только строчные английские буквы
 *
 * Решение - самое быстрое - 0 ms
 *
 * Проход по строке за O(n), где n - длина строки
 * Константное использование дополнительной памяти
 */
class Solution {
    public int[] numberOfLines(int[] widths, String s) {
        int lines = 1; // Минимум одна строка
        int currentWidth = 0; // Текущая ширина текущей строки

        // Проходим по каждому символу в строке
        for (char c : s.toCharArray()) {
            // Получаем ширину текущего символа
            int charWidth = widths[c - 'a'];

            // Проверяем, поместится ли символ на текущей строке
            if (currentWidth + charWidth > 100) {
                // Переносим на новую строку
                lines++;
                currentWidth = charWidth; // Начинаем новую строку с текущего символа
            } else {
                // Добавляем символ к текущей строке
                currentWidth += charWidth;
            }
        }

        return new int[]{lines, currentWidth};
    }
}
