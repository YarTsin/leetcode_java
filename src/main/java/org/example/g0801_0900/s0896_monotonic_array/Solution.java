package org.example.g0801_0900.s0896_monotonic_array;

// #Easy #Array

/**
 * 896. Monotonic Array
 *
 * Дан массив целых чисел nums. Необходимо определить,
 * является ли массив монотонным. Массив является монотонным,
 * если он либо монотонно возрастает, либо монотонно убывает.
 *
 * Определения:
 * Массив nums монотонно возрастает, если для всех i <= j выполняется nums[i] <= nums[j]
 * Массив nums монотонно убывает, если для всех i <= j выполняется nums[i] >= nums[j]
 *
 * Пример 1
 * Вход: nums = [1,2,2,3]  * Выход: true
 * Объяснение: Массив монотонно возрастает (1 ≤ 2 ≤ 2 ≤ 3)
 *
 * Пример 2
 * Вход: nums = [6,5,4,4]  * Выход: true
 * Объяснение: Массив монотонно убывает (6 ≥ 5 ≥ 4 ≥ 4)
 *
 * Пример 3
 * Вход: nums = [1,3,2]  * Выход: false
 * Объяснение: Массив не является монотонным (1 ≤ 3, но 3 ≥ 2)
 *
 * Решение Однопроходная проверка с флагами
 * 3 по скорости решение - 2 ms
 *
 * Сложность:
 * Временная: O(N), где N - длина массива
 * Пространственная: O(1)
 */
class Solution {
    public boolean isMonotonic(int[] nums) {
        boolean isIncreasing = true;
        boolean isDecreasing = true;

        for (int i = 1; i < nums.length; i++) {
            // Проверяем условие возрастания
            if (nums[i] < nums[i - 1]) {
                isIncreasing = false;
            }

            // Проверяем условие убывания
            if (nums[i] > nums[i - 1]) {
                isDecreasing = false;
            }

            // Если оба флага стали false, массив не монотонный
            if (!isIncreasing && !isDecreasing) {
                return false;
            }
        }

        // Массив монотонный, если хотя бы один флаг остался true
        return isIncreasing || isDecreasing;
    }
}